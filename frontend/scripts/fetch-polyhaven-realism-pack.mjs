const LICENSE_TEXT = `Poly Haven Realism Pack
========================
Source: https://polyhaven.com
License: CC0 1.0 (public domain)

This folder contains selected texture maps downloaded from the official Poly Haven API.
The assets are used as optional realism overrides for the virtual world materials.

Generated by: scripts/fetch-polyhaven-realism-pack.mjs
`;

async function fetchJson(url) {
  const response = await fetch(url, {
    headers: {
      'User-Agent': USER_AGENT
    }
  });
  if (!response.ok) {
    throw new Error(`HTTP ${response.status} while fetching ${url}`);
  }
  return response.json();
}

async function downloadFile(url, outPath) {
  if (existsSync(outPath)) {
    return false;
  }

  mkdirSync(dirname(outPath), { recursive: true });
  const response = await fetch(url, {
    headers: {
      'User-Agent': USER_AGENT
    }
  });
  if (!response.ok || !response.body) {
    throw new Error(`Failed download ${url} -> HTTP ${response.status}`);
  }

  await pipeline(Readable.fromWeb(response.body), createWriteStream(outPath));
  return true;
}

function pickFormat(mapEntry) {
  if (!mapEntry) return null;
  for (const fmt of FORMAT_PREFERENCE) {
    if (mapEntry[fmt]?.url) {
      return mapEntry[fmt];
    }
  }
  const first = Object.values(mapEntry)[0];
  return first?.url ? first : null;
}

async function downloadTextureRecipe(recipe) {
  const filesInfo = await fetchJson(`https://api.polyhaven.com/files/${recipe.assetId}`);
  const manifest = {
    slot: recipe.slot,
    assetId: recipe.assetId,
    resolution: RESOLUTION,
    files: {}
  };

  for (const [mapKey, outFile] of Object.entries(recipe.maps)) {
    const mapByResolution = filesInfo?.[mapKey]?.[RESOLUTION];
    if (!mapByResolution) {
      console.warn(`Missing ${mapKey} ${RESOLUTION} for ${recipe.assetId}`);
      continue;
    }
    const picked = pickFormat(mapByResolution);
    if (!picked?.url) {
      console.warn(`No downloadable URL for ${recipe.assetId} ${mapKey} (${RESOLUTION})`);
      continue;
    }

    const outPath = resolve(TARGET_DIR, outFile);
    const downloaded = await downloadFile(picked.url, outPath);
    console.log(`${downloaded ? 'Downloaded' : 'Cached'} ${recipe.assetId}:${mapKey} -> ${outFile}`);

    manifest.files[mapKey] = {
      output: outFile,
      sourceUrl: picked.url,
      md5: picked.md5 || null,
      size: picked.size || null
    };
  }

  return manifest;
}

async function main() {
  mkdirSync(TARGET_DIR, { recursive: true });
  mkdirSync(CACHE_DIR, { recursive: true });

  const manifests = [];
  for (const recipe of TEXTURE_RECIPES) {
    const manifest = await downloadTextureRecipe(recipe);
    manifests.push(manifest);
  }

  writeFileSync(resolve(TARGET_DIR, 'LICENSE.txt'), LICENSE_TEXT, 'utf8');
  writeFileSync(
    resolve(TARGET_DIR, 'manifest.json'),
    JSON.stringify(
      {
        generatedAt: new Date().toISOString(),
        source: 'polyhaven',
        resolution: RESOLUTION,
        textures: manifests
      },
      null,
      2
    ),
    'utf8'
  );
  writeFileSync(
    resolve(CACHE_DIR, `manifest_${RESOLUTION}.json`),
    JSON.stringify(manifests, null, 2),
    'utf8'
  );

  console.log(`Poly Haven realism texture pack ready at: ${TARGET_DIR}`);
}

main().catch((error) => {
  console.error(error);
  process.exit(1);
});
